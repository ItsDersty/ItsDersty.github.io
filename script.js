// –ü–æ–ª—É—á–∏—Ç—å —Å–ª–µ–¥—É—é—â–∏–π –≤–æ–ø—Ä–æ—Å
function getNextQuestion() {
    // –ü—Ä–æ–≤–µ—Ä–∫–∞, –Ω–µ –∑–∞–∫–æ–Ω—á–µ–Ω –ª–∏ —Ç–µ—Å—Ç
    questionCounter++;
    if(availableQuestions.length === 0 || questionCounter > MAX_QUESTIONS) {
       // –¢–µ—Å—Ç –∑–∞–∫–æ–Ω—á–µ–Ω!
       questionElement.innerText = "–¢–µ—Å—Ç –∑–∞–≤–µ—Ä—à–µ–Ω!";
       answersElement.innerHTML = "";
       nextQuestionButton.style.display = 'none';
       resultElement.innerText = `–í–∞—à –∏—Ç–æ–≥–æ–≤—ã–π —Å—á–µ—Ç: ${score} –∏–∑ ${MAX_QUESTIONS}.`;
       return; // –û—Å—Ç–∞–Ω–∞–≤–ª–∏–≤–∞–µ–º —Ñ—É–Ω–∫—Ü–∏—é
    }

    // –í—ã–±–∏—Ä–∞–µ–º —Å–ª—É—á–∞–π–Ω—ã–π –≤–æ–ø—Ä–æ—Å
    const questionIndex = Math.floor(Math.random() * availableQuestions.length);
    currentQuestion = availableQuestions[questionIndex];
    questionElement.innerText = currentQuestion.front;

    // –£–¥–∞–ª—è–µ–º –≤—ã–±—Ä–∞–Ω–Ω—ã–π –≤–æ–ø—Ä–æ—Å –∏–∑ –¥–æ—Å—Ç—É–ø–Ω—ã—Ö
    availableQuestions.splice(questionIndex, 1);

    // –ì–µ–Ω–µ—Ä–∏—Ä—É–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã –æ—Ç–≤–µ—Ç–∞
    generateAnswerChoices();
}

// –°–≥–µ–Ω–µ—Ä–∏—Ä–æ–≤–∞—Ç—å –≤–∞—Ä–∏–∞–Ω—Ç—ã –æ—Ç–≤–µ—Ç–∞
function generateAnswerChoices() {
    const answers = [];
    const correctAnswer = currentQuestion.back; // –ü—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç
    answers.push(correctAnswer);

    // –î–æ–±–∞–≤–ª—è–µ–º 3 –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã—Ö –æ—Ç–≤–µ—Ç–∞ (–¥–∏—Å—Ç—Ä–∞–∫—Ç–æ—Ä–∞)
    for (let i = 0; i < 3; i++) {
        let randomIndex = Math.floor(Math.random() * flashcardsData.length);
        let wrongAnswer = flashcardsData[randomIndex].back;

        // –£–±–µ–¥–∏–º—Å—è, —á—Ç–æ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–π –æ—Ç–≤–µ—Ç –Ω–µ —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å –ø—Ä–∞–≤–∏–ª—å–Ω—ã–º –∏–ª–∏ –¥—Ä—É–≥–∏–º–∏ –Ω–µ–ø—Ä–∞–≤–∏–ª—å–Ω—ã–º–∏
        while (wrongAnswer === correctAnswer || answers.includes(wrongAnswer)) {
            randomIndex = Math.floor(Math.random() * flashcardsData.length);
            wrongAnswer = flashcardsData[randomIndex].back;
        }

        answers.push(wrongAnswer);
    }

    // –ü–µ—Ä–µ–º–µ—à–∏–≤–∞–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã –æ—Ç–≤–µ—Ç–∞ —Å–ª—É—á–∞–π–Ω—ã–º –æ–±—Ä–∞–∑–æ–º
    shuffleArray(answers);

    // –û—Ç–æ–±—Ä–∞–∂–∞–µ–º –≤–∞—Ä–∏–∞–Ω—Ç—ã –æ—Ç–≤–µ—Ç–∞ –Ω–∞ —Å—Ç—Ä–∞–Ω–∏—Ü–µ
    answersElement.innerHTML = ""; // –û—á–∏—â–∞–µ–º –ø—Ä–µ–¥—ã–¥—É—â–∏–µ –≤–∞—Ä–∏–∞–Ω—Ç—ã
    answers.forEach((answer, index) => {
        const button = document.createElement('button');
        button.classList.add('answer-button');
        button.innerText = answer;
        button.dataset.index = index; // –°–æ—Ö—Ä–∞–Ω—è–µ–º –∏–Ω–¥–µ–∫—Å
        button.addEventListener('click', checkAnswer); // –î–æ–±–∞–≤–ª—è–µ–º –æ–±—Ä–∞–±–æ—Ç—á–∏–∫
        answersElement.appendChild(button);
    });
}

// –§—É–Ω–∫—Ü–∏—è –ø–µ—Ä–µ–º–µ—à–∏–≤–∞–Ω–∏—è –º–∞—Å—Å–∏–≤–∞ (Fisher-Yates shuffle)
function shuffleArray(array) {
    for (let i = array.length - 1; i > 0; i--) {
        const j = Math.floor(Math.random() * (i + 1));
        [array[i], array[j]] = [array[j], array[i]];
    }
}

